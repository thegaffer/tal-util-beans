package org.talframework.util.beans.binding;

import java.util.List;

public interface BindingResult<T> {

    /**
     * @return The object we have bound into
     */
    public T getTarget();
    
    /**
     * @return True if the result has any errors at all, false otherwise
     */
    public boolean hasErrors();
    
    /**
     * @return Any non-fatal errors in the binding
     */
    public List<BindingError> getErrors();
}
